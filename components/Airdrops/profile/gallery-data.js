function getLongestArray(...arrays) {
  let longestArray = arrays[0]; // Assume the first array is the longest

  for (let i = 1; i < arrays.length; i += 1) {
    if (arrays[i].length > longestArray.length) {
      longestArray = arrays[i];
    }
  }

  return longestArray;
}

